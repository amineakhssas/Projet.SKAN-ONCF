    private void GtTrajetTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_GtTrajetTableMouseClicked
        // TODO add your handling code here:
        int row = GtTrajetTable.getSelectedRow();
        String gareDepart = GtTrajetTable.getModel().getValueAt(row, 0).toString();
        String gareArrivee = GtTrajetTable.getModel().getValueAt(row, 1).toString();
        String dateDepart = GtTrajetTable.getModel().getValueAt(row, 2).toString();
        String heureDepart = GtTrajetTable.getModel().getValueAt(row, 3).toString();
        String confort = GtTrajetTable.getModel().getValueAt(row, 4).toString();
        if (confort.equals("1")) {
            confort = "2éme classe";
        } else {
            confort = "1er classe";
        }

        ticket = new Ticket(gareDepart, gareArrivee, dateDepart, heureDepart, confort);
    }//GEN-LAST:event_GtTrajetTableMouseClicked

