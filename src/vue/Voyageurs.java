    private void AfficherTrajet() {
        try {
            java.sql.Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/trajets?useUnicode=true&useJDBCCompliantTimezoneShift=true&useLegacyDatetimeCode=false&serverTimezone=Africa/Casablanca", "root", "");
            Statement St = connection.createStatement();
            String sql = "select gareDepart, gareArrivee, dateDepart,heureDepart, confort from trajetstable where gareDepart = ? and gareArrivee = ? and dateDepart >=  ? and nbrPlaces >0 and confort = ?  ";
            PreparedStatement prest = connection.prepareStatement(sql);
            prest.setString(1, gareDepart.getSelectedItem().toString());
            prest.setString(2, gareArrivee.getSelectedItem().toString());
            prest.setInt(4, confort.getSelectedIndex());
            prest.setString(3, date);

            ResultSet result = prest.executeQuery();
            DefaultTableModel model = (DefaultTableModel) GtTrajetTable.getModel();
            model.setRowCount(0); // Clear existing data

            ResultSetMetaData rsmd = result.getMetaData();
            int columnCount = rsmd.getColumnCount();

            // Ensure model has the correct columns, only if it's empty
            if (model.getColumnCount() == 0) {
                for (int i = 1; i <= columnCount; i++) {
                    model.addColumn(rsmd.getColumnName(i));
                }
            }

            // Add data rows to the model
            while (result.next()) {
                Object[] row = new Object[columnCount];
                for (int i = 1; i <= columnCount; i++) {
                    row[i - 1] = result.getObject(i);
                }
                model.addRow(row);
            }
            result.close();
            prest.close();
            connection.close();
        } catch (Exception e) {
            System.out.println(" exception" + e.toString());
            e.printStackTrace();
        }

    }
